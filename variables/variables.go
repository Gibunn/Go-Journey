package main

import "fmt"

func main() {
	var a = "Init"
	fmt.Println(a)

	var b, c int = 1, 2
	fmt.Println(b, c)

	var d = true
	fmt.Println(d)

	var e int
	fmt.Println(e)

	var f string
	fmt.Println(f)

	var g bool
	fmt.Println(g)

	var h float32
	fmt.Println(h)

	i := "Strawberries"
	fmt.Println(i)
}